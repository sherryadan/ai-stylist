export const getProductsByBrand = (brand: string): Product[] =>
  products.filter((p) => p.brand.toLowerCase() === brand.toLowerCase());

export const getProductsByCategory = (category: string): Product[] =>
  products.filter((p) => p.category === category);

export const getProductsByStyle = (style: string): Product[] =>
  products.filter((p) => p.styleTags.includes(style as never));

export const getProductsByOccasion = (occasion: string): Product[] =>
  products.filter((p) => p.occasionTags.includes(occasion as never));

export const getProductsByBudget = (min: number, max: number): Product[] =>
  products.filter((p) => p.price >= min && p.price <= max);

export const getProductById = (id: string): Product | undefined =>
  products.find((p) => p.id === id);
